package com.ntconsult.desafio.model;

public class ResultadoVotacao {
	private int sim;
	private int nao;
	private int total;
	public int getSim() {
		return sim;
	}
	public void setSim(int sim) {
		this.sim = sim;
	}
	public int getNao() {
		return nao;
	}
	public void setNao(int nao) {
		this.nao = nao;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
}
